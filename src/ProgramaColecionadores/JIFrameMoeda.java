 private void listarMoeda(){
        DefaultTableModel dtm = (DefaultTableModel) jtbListarMoeda.getModel();
        dtm.setNumRows(0);
        
        for (Moeda moedaTable :  Dados.getUsuariosBD().get(idUserLogando).getColecaoMoeda()) {
            dtm.addRow(new Object[]{moedaTable.getIdItem(),moedaTable.getNomeM(), moedaTable.getEdicao()});
            
        }
    }

