    private void SalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SalvarMouseClicked
        Moeda novaMoeda=moeda;
        //dentro da classe moeda
        novaMoeda.setPrivacidade(this.jComboBoxPrivacidade.getSelectedIndex());
        novaMoeda.setNomeM(this.jTextFieldNomeM.getText());
        novaMoeda.setPesoM(this.jTextFieldPesoM.getText());
        novaMoeda.setDiametroM(this.jTextFieldDiametroM.getText());
        novaMoeda.setEspessuraM(this.jTextFieldEspessuraM.getText());
        novaMoeda.setMetodoCunhagemM(this.jTextFieldMetodoCunhagemM.getText());
        novaMoeda.setIdMaterial(this.jComboBoxMaterialM.getSelectedIndex());
        novaMoeda.setCoresM(this.jComboBoxCoresM.getSelectedItem().toString());
        novaMoeda.setBordaM(this.jComboBoxBordasM.getSelectedItem().toString());
        //dentro da classe item
        novaMoeda.setEdicao(this.jTextFieldEdicao.getText());
        novaMoeda.setEstadoPreservacao(this.jComboBoxEstado.getSelectedItem().toString());
        novaMoeda.setDescricao(this.jTextFieldDescricao.getText());
        novaMoeda.setFabricacao(this.jTextFieldFabricacao.getText());
        novaMoeda.setAquisicao(this.jTextFieldAquisicao.getText());
        novaMoeda.setVFabricacao(jTextFieldVFabricacao1.getText());
        novaMoeda.setVAquisicao(jTextFieldVAquisicao1.getText());
        novaMoeda.setFoto(img);
        //armazena o ID do item
        Dados.getMoedasBD().get(atividadeEditar.getIdItem()).equals(novaMoeda);
        atividadeEditar.add(novaMoeda);
        atividadeEditar.bloquearAtividades(idUserLogando);
        JIFrameListarMoeda jIFrameListarMoeda = new JIFrameListarMoeda(idUserLogando);
        jIFrameListarMoeda.idUserLogando=Principal.getIdUserLogando();
        this.getDesktopPane().add(jIFrameListarMoeda);
        jIFrameListarMoeda.setVisible(true);
        try {jIFrameListarMoeda.setMaximum(true);
        } catch (PropertyVetoException ex) {  }
        this.doDefaultCloseAction();//fim atual
    }//GEN-LAST:event_SalvarMouseClicked

